/// Utility class that holds spacing values used throughout the app.
/// Using consistent spacing makes the UI look more professional.
class AppSpacing {
  // Private constructor - we only use static members
  AppSpacing._();

  // Spacing values from extra small to extra large
  static const double xs = 6;
  static const double sm = 10;
  static const double md = 16;
  static const double lg = 24;
  static const double xl = 32;
}
